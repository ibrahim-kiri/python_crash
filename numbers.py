# Integers
add = 2 + 3
sub = 3 - 2
mult = 2 * 3
div = 3 / 2
print(add, sub, mult, div, sep="\n")

exp = 3 ** 2
exp1 = 3 ** 3
exp2 = 10 ** 6
print(exp, exp1, exp2, sep="\n")

oper = 2 + 3*4
oper1 = (2 + 3) * 4
print(oper, oper1, sep="\n")

# Floats
floa = 0.1 + 0.1
floa1 = 0.2 + 0.1
floa2 = 3 * 0.1
floa3 = 2 * 0.2
print(floa, floa1, floa2, floa3, sep="\n")

# Integers and Floats
int_floa = 4 / 2
int_floa1 = 1 + 2.0
int_floa2 = 2 * 3.0
int_floa3 = 3.0 ** 2
print(int(int_floa), int_floa1, int_floa2, int_floa3, sep="\n")

# Underscores in numbers
universe_age = 14_000_000
print(universe_age)

# Multiple assignment
x, y, z = 0, 1, 3
print(x, y, z, sep="\n")

# Constants
MAX_CONNECTIONS = 5000
print(MAX_CONNECTIONS)
